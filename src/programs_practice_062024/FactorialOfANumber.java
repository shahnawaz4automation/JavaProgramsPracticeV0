package programs_practice_062024;

import java.util.Collections;

public class FactorialOfANumber {
	public static void main(String[] args) {
		int i = 0;
		int factorial = 1;
		for(int j=1;j<=i;j++) {
			factorial = factorial*j;  
		}
		System.out.println("Factorial of "+i+" is: "+factorial);
		
	}
}
